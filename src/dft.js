var complex = require('./complex');
var fftUtil = require('./fftutil');

//-------------------------------------------------
// Calculate brute-force O(n^2) DFT for vector.
//-------------------------------------------------
var dft = function(vector) {
  var X = [],
      N = vector.length;

  for (var k = 0; k < N; k++) {
    X[k] = [0, 0]; //Initialize to a 0-valued complex number.

    for (var i = 0; i < N; i++) {
      var exp = fftUtil.exponent(k * i, N);
      X[k] = complex.add(X[k], exp); //Complex summation of X[k] and exponential
      //X[k] = complex.multiply(X[k], [2, 0]);
    }
  }

  return X;
};

module.exports = dft;